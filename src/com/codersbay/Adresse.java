package com.codersbay;

public class Adresse {

    public int postCode;
    public String location;
    public String postcode;
    public int number;

    public Adresse(int postCode, String location, String postcode, int number) {
        this.postCode = postCode;
        this.location = location;
        this.postcode = postcode;
        this.number = number;
    }
}
