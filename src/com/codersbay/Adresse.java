package com.codersbay;

public class Adresse {

    public int postCode;
    public String location;
    public String adress;
    public int number;

    public Adresse(int postCode, String location, String adress, int number) {
        this.postCode = postCode;
        this.location = location;
        this.adress = adress;
        this.number = number;
    }
}
